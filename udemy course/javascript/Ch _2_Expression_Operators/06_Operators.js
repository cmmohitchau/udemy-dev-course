//  Comparison Operator
let d=5
let e=6
console.log("d==e",d==e)  //false
console.log("d!=e",d!=e)  //true
console.log("d===e",d===e)   //true   it compare type and value
console.log("d!==e",d!==e)   //false  it compare type and value
console.log("d<=e",d<=e)   //true
console.log("d>=e",d>=e)   //false
console.log("d>e",d>e)   //false
console.log("d<e",d<e)   //true

