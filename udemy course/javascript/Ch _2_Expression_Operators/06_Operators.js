// Operators in js
console.log("Operators in js")

// Arithmetic Operator
let a=10;
let b=3;

console.log("a+b=",a+b) //13
console.log("a-b=",a-b) //7
console.log("a*b=",a*b) //30
console.log("a/b=",a/b) //3.33
console.log("a%b=",a%b) //1
console.log("a**b=",a**b) //1000
console.log("a++=",a++) //10
console.log("a=",a) //11
console.log("a--=",a--) //11
console.log("a+b=",a+b) //10
console.log("++a=",++a) //11
console.log("--b=",--b) //2

